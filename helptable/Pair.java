package helptable;

public class Pair {
	private String first;
	private String second;
	public Pair(){
		first = "";
		second = "";
	}
	public Pair(String f, String s){
		first = f;
		second = s;
	}
	public String First() {
		return first;
	}
	public String Second() {
		return second;
	}

}
